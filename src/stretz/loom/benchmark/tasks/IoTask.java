    public IoTask(int fileNumber, int folderNumber) {
        this.fileNumber = fileNumber;
        this.folderNumber = folderNumber;
        this.state = TaskState.NOT_STARTED;
    }


    @Override
    public TaskState execute() {
        this.state = TaskState.ACTIVE;
        ThreadPoolExecutor executor = (ThreadPoolExecutor) Executors.newCachedThreadPool();
        for (int i = 1; i <= fileNumber; i++) {
            FileCreator creator = new FileCreator(this.path + i, ".txt", this.text);
            executor.execute(creator);
        }
        shutdownAndAwaitTermination(executor);
        this.state = TaskState.DONE;
        return TaskState.DONE;
    }

    @Override
    public TaskState getState() {
        return this.state;
    }

    /**
     * Shuts down the ThreadPoolExecutor
     * @TODO needs to be adjusted for own use
     * @param pool executor
     */
    static void shutdownAndAwaitTermination(ThreadPoolExecutor pool) {
        pool.shutdown();

        try {
            if(!pool.awaitTermination(60, TimeUnit.SECONDS)) {
                pool.shutdownNow();

                if(!pool.awaitTermination(60, TimeUnit.SECONDS)) {
                    System.err.println("Pool did not Terminate");
                }
            }
        } catch (InterruptedException e) {
            pool.shutdownNow();
            Thread.currentThread().interrupt();
        }
    }
